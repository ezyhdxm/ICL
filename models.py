class LeastSquaresModel:
    def __init__(self, driver=None):
        self.driver = driver
        self.name = f"OLS_driver={driver}"

    def __call__(self, xs, ys, inds=None):
        xs, ys = xs.cpu(), ys.cpu()
        if inds is None:
            inds = range(ys.shape[1])
        
        preds = []

        for i in inds:
            if i == 0:
                preds.append(torch.zeros_like(ys[:, i]))
                continue
            train_xs, train_ys = xs[:, :i], ys[:, i]
            test_x = xs[:, i]

            ws, _, _ = torch.linalg.lstsq(train_xs, train_ys.unsqueeze(2), driver=self.driver)

            pred = test_x @ ws
            preds.append(pred[:,0,0])
        return torch.stack(preds, dim=1)